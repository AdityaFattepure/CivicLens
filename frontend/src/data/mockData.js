// Helper to flatten tree for quick lookup
function flattenTree(nodes, parentChain = []) {
  const result = {}
  for (const node of nodes) {
    const chain = [...parentChain, { id: node.id, name: node.name, type: node.type }]
    result[node.id] = { ...node, chain }
    if (node.children?.length) {
      Object.assign(result, flattenTree(node.children, chain))
    }
  }
  return result
}
